//For loop
for (var i = 0; i < 10; i++) {
    console.log(i);
}

//While loop
var i = 0;
while (i < 10) {
    console.log(i);
    i++;
}

//Advanced for loop
var arr = [1, 2, 3, 4, 5];
for (var i = 0; i < arr.length; i++) {
    console.log(arr[i]);
}


//Advanced while loop
var arr = [1, 2, 3, 4, 5];
var i = 0;
while (i < arr.length) {
    console.log(arr[i]);
    i++;
}

//do while loop
var i = 100;
do {
    console.log(i);
    i++;
} while (i < 100);